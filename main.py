# ---------------------------------------------------------------------------
# Lifespan (startup / shutdown)
# ---------------------------------------------------------------------------

@asynccontextmanager
async def lifespan(app: FastAPI):
    """Configure Cognee and start file watcher on startup; clean up on shutdown."""
    _configure_cognee()
    # Initialize Cognee (creates SQLite + Kuzu DBs if not present)
    try:
        await cognee.prune.prune_system(metadata=False)  # non-destructive init ping
    except Exception:
        pass  # First run — no data to prune, that's fine

    # Start file watcher in background thread
    watch_dir = os.getenv("WATCHED_DIR", "./watched_runs")
    loop = asyncio.get_event_loop()
    try:
        from watcher import start_watcher
        start_watcher(watch_dir, loop, dataset_name="main_dataset")
        logger.info("File watcher started on: %s", os.path.abspath(watch_dir))
    except Exception as e:
        logger.warning("File watcher failed to start: %s", e)

    yield  # Server is running

    # Shutdown
    try:
        from watcher import stop_watcher
        stop_watcher()
    except Exception:
        pass


