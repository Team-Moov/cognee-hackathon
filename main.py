@app.post("/remember", response_model=RememberResponse, tags=["Core"])
async def remember(req: RememberRequest):
    """
    Ingest a full ML run document into the memory graph.

    Returns node IDs and config_hash immediately so the caller can build
    lineage links without another round-trip.
    """
    global _run_counter

    from memory import remember_run, scan_and_register_artifacts

    try:
        run_data = req.model_dump(exclude={"dataset"})
        result = await remember_run(run_data, dataset_name=req.dataset)

        # Register artifacts in in-memory registry
        for fpath in result.get("artifact_paths", []):
            _artifact_registry[fpath] = {
                "result_id": result["node_id"],
                "artifact_type": _infer_artifact_type(fpath),
                "description": f"Auto-indexed artifact from run {result['node_id'][:12]}",
                "exists_on_disk": os.path.exists(fpath),
            }

        _run_counter += 1
        improved = False
        if _run_counter % IMPROVE_EVERY_N == 0:
            logger.info("Auto-triggering improve_memory (run_counter=%d)", _run_counter)
            asyncio.create_task(_auto_improve(req.dataset))
            improved = True

        return RememberResponse(**result, improved=improved)
    except Exception as e:
        logger.exception("remember() failed")
        _error(500, "ingestion_failed", str(e))


async def _auto_improve(dataset_name: str):
    from memory import improve_memory
    try:
        await improve_memory(dataset_name)
        logger.info("Auto-improve completed for dataset=%s", dataset_name)
    except Exception as e:
        logger.error("Auto-improve failed: %s", e)


def _infer_artifact_type(path: str) -> str:
    ext = os.path.splitext(path)[1].lower()
    mapping = {
        ".pt": "checkpoint", ".pth": "checkpoint", ".ckpt": "checkpoint",
        ".png": "plot", ".jpg": "plot", ".pdf": "plot", ".svg": "plot",
        ".log": "log", ".txt": "log",
        ".json": "eval_report", ".csv": "eval_report",
    }
    return mapping.get(ext, "other")


