@app.get("/find-file", response_model=FindFileResponse, tags=["Artifacts"])
async def find_file(description: str = Query(..., description="Text description of the artifact to find")):
    """
    Find an artifact file by semantic description.

    Searches the registered artifact registry and the memory graph.
    Returns the file path, artifact ID, and whether the file still exists.
    """
    # First check in-memory registry with token overlap
    from memory import _rough_similarity
    best_score = 0.0
    best_fpath = None
    for fpath, info in _artifact_registry.items():
        score = _rough_similarity(description, info.get("description", ""))
        if score > best_score:
            best_score = score
            best_fpath = fpath

    if best_fpath and best_score > 0.1:
        info = _artifact_registry[best_fpath]
        return FindFileResponse(
            file_path=best_fpath,
            artifact_id=best_fpath,
            exists_on_disk=os.path.exists(best_fpath),
            description=info.get("description", ""),
            artifact_type=info.get("artifact_type", "other"),
        )

    # Fall back to graph query
    from memory import query_memory
    result = await query_memory(f"Find artifact file: {description}")
    if result.get("sources"):
        src = result["sources"][0]
        return FindFileResponse(
            file_path=str(src),
            artifact_id=str(src),
            exists_on_disk=os.path.exists(str(src)),
            description=result.get("answer", "")[:200],
            artifact_type="other",
        )

    raise HTTPException(
        status_code=404,
        detail={"error": "artifact_not_found", "detail": f"No artifact matching: {description}"},
    )


