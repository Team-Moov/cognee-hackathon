# GET /orphans
# ---------------------------------------------------------------------------

@app.get("/orphans", response_model=OrphansResponse, tags=["Artifacts"])
async def orphans():
    """
    Return two lists:
    1. Files on disk with no matching Artifact node (untracked_files)
    2. Artifact nodes whose file_path no longer exists on disk (broken_references)

    Includes total size in bytes for each list — useful for "X MB of untracked files" demo beat.
    """
    watch_dir = os.path.abspath(os.getenv("WATCHED_DIR", "./watched_runs"))
    os.makedirs(watch_dir, exist_ok=True)

    known_paths = set(_artifact_registry.keys())

    # Walk disk to find untracked files
    untracked: List[ArtifactInfo] = []
    for root, _dirs, files in os.walk(watch_dir):
        for fname in files:
            fpath = os.path.abspath(os.path.join(root, fname))
            if fpath not in known_paths:
                size = 0
                try:
                    size = os.path.getsize(fpath)
                except OSError:
                    pass
                untracked.append(ArtifactInfo(
                    file_path=fpath,
                    size_bytes=size,
                    artifact_type=_infer_artifact_type(fpath),
                    result_id=None,
                ))

    # Find broken references (registered but no longer on disk)
    broken: List[ArtifactInfo] = []
    for fpath, info in _artifact_registry.items():
        if not os.path.exists(fpath):
            broken.append(ArtifactInfo(
                file_path=fpath,
                size_bytes=0,  # file is gone, no size
                artifact_type=info.get("artifact_type", "other"),
                result_id=info.get("result_id"),
            ))

    total_untracked = sum(a.size_bytes for a in untracked)
    total_broken = sum(a.size_bytes for a in broken)

    return OrphansResponse(
        untracked_files=untracked,
        broken_references=broken,
        total_untracked_size_bytes=total_untracked,
        total_broken_size_bytes=total_broken,
        untracked_count=len(untracked),
        broken_count=len(broken),
    )


